// while
let index = 1
while (index <= 10) {
    console.log(`The value of index is ${index}`);
    index ++;
}

let myArray = ["zaid", "faizan", "suhaib", "raqib"];
let arr = 0;

while (arr < myArray.length) {
    console.log(`The array is ${myArray[arr]}`);
    arr ++;
}

//do-while loop
let score = 1
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);
